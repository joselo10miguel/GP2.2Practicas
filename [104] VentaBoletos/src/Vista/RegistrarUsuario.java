    private void tblUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuarioMouseClicked
        // TODO add your handling code here:
        int fila=tblUsuario.getSelectedRow();
        
        if(fila>=0){
            txtCedula.setText(tblUsuario.getValueAt(fila, 0).toString());
            txtNombre1.setText(tblUsuario.getValueAt(fila, 1).toString());            
            txtNombre2.setText(tblUsuario.getValueAt(fila,2).toString());
            txtApellido1.setText(tblUsuario.getValueAt(fila, 3).toString());
            txtApellido2.setText(tblUsuario.getValueAt(fila, 4).toString());
            txtUsername.setText(tblUsuario.getValueAt(fila, 5).toString());
            txtPassword.setText(tblUsuario.getValueAt(fila, 6).toString());
        }
        else{
            JOptionPane.showMessageDialog(null, "No selecciono fila");
        }
    }//GEN-LAST:event_tblUsuarioMouseClicked

