    void mostrarDatos(String valor){
        
        Conexion conx=new Conexion();
        DefaultTableModel modelo=new DefaultTableModel();
        modelo.addColumn("Cedual");
        modelo.addColumn("1 Nombre ");
        modelo.addColumn("2 Nombre ");
        modelo.addColumn("1 Apellido ");
        modelo.addColumn("2 Apellido");
        modelo.addColumn("Usuario");
        modelo.addColumn("Password");
        
        tblUsuario.setModel(modelo);
        String sql="";
        if(valor.equals("")){
            sql="SELECT * FROM usuario";
        }
        else{
            sql="SELECT * FROM usuario WHERE Cedula='"+valor+"'";
        }
        String []datos=new String[7];
        try {
            Statement st= conx.getConexion().createStatement();
            ResultSet rs=st.executeQuery(sql);
            while(rs.next()){
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
                datos[5]=rs.getString(6);
                datos[6]=rs.getString(7);
                modelo.addRow(datos);
            }
            tblUsuario.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(RegistrarUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

