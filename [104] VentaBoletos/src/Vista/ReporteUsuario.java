    void mostrarDatos(String valor){
        
        Conexion conx=new Conexion();
        DefaultTableModel modelo=new DefaultTableModel();
        modelo.addColumn("Cedula");
        modelo.addColumn("1ยบ Nombre ");
        modelo.addColumn("2ยบ Nombre ");
        modelo.addColumn("1ยบ Apellido");
        modelo.addColumn("2ยบ Apellido");
        modelo.addColumn("Username ");
              
        
        tblBoletoVendido.setModel(modelo);
        String sql="";
        if(valor.equals("")){
            sql="SELECT * FROM usuario";
        }
        else{
            sql="SELECT * FROM usuario WHERE Cedula='"+valor+"'";
        }
        String []datos=new String[6];
        try {
            Statement st= conx.getConexion().createStatement();
            ResultSet rs=st.executeQuery(sql);
            while(rs.next()){
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
                datos[5]=rs.getString(6);
                modelo.addRow(datos);
            }
            tblBoletoVendido.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(RegistrarRuta.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

